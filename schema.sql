CREATE TABLE IF NOT EXISTS users (
  id SERIAL PRIMARY KEY,
  code VARCHAR(30) UNIQUE NOT NULL,
  name VARCHAR(150) NOT NULL,
  password_hash VARCHAR(100) NOT NULL,
  must_change_password BOOLEAN DEFAULT TRUE,
  role VARCHAR(20) DEFAULT 'worker',
  created_at TIMESTAMP DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS reports (
  id SERIAL PRIMARY KEY,
  worker_code VARCHAR(30) NOT NULL REFERENCES users(code) ON DELETE CASCADE,
  report_date DATE NOT NULL,
  created_at TIMESTAMP DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS report_lines (
  id SERIAL PRIMARY KEY,
  report_id INTEGER NOT NULL REFERENCES reports(id) ON DELETE CASCADE,
  project VARCHAR(10) NOT NULL,
  product INTEGER NOT NULL,
  stage VARCHAR(10) NOT NULL,
  contractor_code VARCHAR(30),
  note TEXT DEFAULT '',
  created_at TIMESTAMP DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS email_recipients (
  id SERIAL PRIMARY KEY,
  email VARCHAR(100) UNIQUE NOT NULL,
  created_at TIMESTAMP DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_reports_worker ON reports(worker_code);
CREATE INDEX IF NOT EXISTS idx_reports_date ON reports(report_date);
CREATE INDEX IF NOT EXISTS idx_lines_report ON report_lines(report_id);
CREATE INDEX IF NOT EXISTS idx_lines_project ON report_lines(project, product);

